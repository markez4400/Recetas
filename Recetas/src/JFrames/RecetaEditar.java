    public void mostrarValoresAntiguos() {
        int index = -1;
        
        switch (tipo) {
            case "Entrante":
                index = 1;
                break;
            case "Primero":
                index = 2;
                break;
            case "Segundo":
                index = 3;
                break;
            case "Combinado":
                index = 4;
                break;
            case "Postre":
                index = 5;
                break;
            case "Otro":
                index = 6;
            default:
                index = 0;
                break;
        }

        //mostrar los valores antiguos en las celdas
        LabelEditIdRE.setText(String.valueOf(id));
        TxtFieldNombreRE.setText(nombre);
        ComboBoxTipoRE.setSelectedIndex(index);
        TxtAreaIngredientesRE.setText(ingredientes);
        TxtAreaPreparacionRE.setText(preparacion);
        LblFechaMostrarRE.setText(fecha);
        LblUltimaModificacionMostrarRE.setText(ultimaModificacion);
    }

    public void modificarReceta() {
        //declaro variables
        String nombreE = "", tipoE = "", ingredientesE = "", preparacionE = "";
        int idE;
        boolean todoOk = false;

        //recogo valores de los campos y los guardo en las variables
        idE = Integer.parseInt(LabelEditIdRE.getText());
        nombreE = TxtFieldNombreRE.getText().replace("'", "''"); //para que se puedan agregar apostrofes
        tipoE = ComboBoxTipoRE.getItemAt(ComboBoxTipoRE.getSelectedIndex());
        ingredientesE = TxtAreaIngredientesRE.getText().replace("'", "''");;
        preparacionE = TxtAreaPreparacionRE.getText().replace("'", "''");;
        Date ultimaModificacionE = RecetaNueva.fechaActualDate();

        
        //se comprueban las variables
        if (RecetaNueva.comprovarVariables(nombreE, tipoE, ingredientesE, preparacionE)) {
            //guardar datos en base de datos usando UPDATE
            //la fecha no se toca nunca, (el id creo que tampoco)
            String SQL = "UPDATE recetas SET nombre = '" + nombreE + "', "
                    + "tipo = '" + tipoE + "', ingredientes = '" + ingredientesE + "',"
                    + " preparacion = '"+preparacionE+"', ultima_modificacion = '"+ultimaModificacionE+"' WHERE id = '" + idE + "'";
            cc.actualizarReceta(SQL, con);
            
            //vuelvo a recetas
            Recetas abrir = new Recetas();
            abrir.setVisible(true);
            this.dispose();
        }
        
    }
    
    
    
        
        
    

